//using while loop print alphabets from A to Z
//class 
class Alphabets
  //main method
{
  public static void main(String[] args)
  {
    //initalizing character
   char ch;
    //this condition brings all alphabets from a to z
    for(ch='A';ch<='Z';ch++)
      {
        System.out.println(ch);
      }
    
  }
}